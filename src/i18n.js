import i18n from "i18next";
import { initReactI18next } from "react-i18next";

import es from "./locales/es.json";
import en from "./locales/en.json";

const resources = {
  en: {
    translation: en,
  },
  es: {
    translation: es,
  },
};

i18n.use(initReactI18next).init({
  resources,
  lng: (navigator.language || navigator.userLanguage || "en").split("-")[0],
  interpolate: {
    escapeValue: false,
  },
});

export default i18n;
